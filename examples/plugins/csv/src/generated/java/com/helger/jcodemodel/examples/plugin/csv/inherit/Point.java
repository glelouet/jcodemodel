package com.helger.jcodemodel.examples.plugin.csv.inherit;

public class Point {
    private int x;
    private int y;

    /**
     * set the {@link #x}
     */
    public void setX(int x) {
        this.x = x;
    }

    /**
     * @return the {@link #x}
     */
    public int getX() {
        return x;
    }

    /**
     * set the {@link #y}
     */
    public void setY(int y) {
        this.y = y;
    }

    /**
     * @return the {@link #y}
     */
    public int getY() {
        return y;
    }
}
